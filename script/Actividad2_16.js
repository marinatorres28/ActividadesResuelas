/* Desarrolla un programa donde expliques las diferencias de usar los operadores a++
a--,++a,--a */

let a = 1;


console.log('[a] Valor inicial -> ' + a);
console.log('[a++] Mostramos el valor por pantalla y después lo incrementamos -> ' + a++);
console.log('[a] Mostramos el valor por pantalla ' + a);
console.log('[++a] Incrementamos el numero y después lo mostramos ' + ++a);

let b = 1;

console.log("[b] Valor inicial -> " + b);
console.log("[b--] Mostramos el valor por pantalla y despues lo disminumos -> " + b--);
console.log("[b] Mostramos el valor por pantalla -> " + b);
console.log("[--b] Decrementamos el numero y despues lo mostramos -> " + --b);